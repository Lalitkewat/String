package string;



/**
 * @author Lalit.
 * Write a program calculate numbers using String math method. 
 */
public class Maths {

	public static void main(String[] args) {
		System.out.println("Maximum3,5="+Math.max(3,5));
		System.out.println("Minimum3,5="+Math.min(3, 5));
		System.out.println("Absolute3.7="+Math.abs(3.7)); 
		System.out.println("Exp 10="+Math.exp(10));
		System.out.println("random number="+Math.random());
		System.out.println("Square root="+Math.sqrt(4));

	}

}
